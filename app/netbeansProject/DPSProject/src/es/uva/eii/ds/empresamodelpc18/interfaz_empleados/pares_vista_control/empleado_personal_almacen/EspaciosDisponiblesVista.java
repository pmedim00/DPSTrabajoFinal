    /**
     * Asignar espacios
     * @param numEspacios numero de espacios
     */
    public void setNumEspacios(int numEspacios) {
        this.numEspacios.setText(String.valueOf(numEspacios));
    }
    
    /**
     * Asignar tipo de cpu
     * @param tipo tipo a asignar
     */
    public void setTipo(String tipo) {
        this.tipo.setText(tipo);
    }

    /**
     * Obtener espacios
     * @return numero de espacios
     */
    public int getNumEspacios() {
        int num = Integer.parseInt(numEspacios.getText());
        return num;
    }

    /**
     * Obtener tipo de cpu
     * @return cpu
     */
    public int getTipoCPU() {
        String tip=tipo.getText();
        int numCPU=1;
        if (tip.equals("Intel")){
            numCPU=2;
        }
        return numCPU;
    }
    
    /**
     * Actualiza la tabla de resultados
     * @param seccion numero
     * @param zona numero
     * @param estanteria numero
     * @param ocupado muestra si esta ocupado o no
     */
    public void mostrarEspaciosDisponibles(int seccion, int zona, int estanteria, String ocupado) {
        DefaultTableModel model = (DefaultTableModel) tablaEspacios.getModel();
        model.addRow(new Object[]{seccion, zona, estanteria, ocupado});
    }

    /**
     * Mensaje de error
     * @param error 
     */
    void errorMessage(String error) {
        errorLabel.setText(error);
    }


